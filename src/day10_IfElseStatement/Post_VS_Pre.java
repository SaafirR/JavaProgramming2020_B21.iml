package day10_IfElseStatement;

public class Post_VS_Pre {

    public static void main(String[] args) {
        // post:
        int x = 7;

        System.out.println(++x); // 8
        System.out.println(x); //8

        System.out.println(--x); // 7
        System.out.println(x); // 7


    }
}
