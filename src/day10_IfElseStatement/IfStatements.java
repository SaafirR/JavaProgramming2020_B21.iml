package day10_IfElseStatement;

public class IfStatements {

    public static void main(String[] args) {

        int score = 45;

        if(score >= 60){
            System.out.println("Congrats");
        }

        if(score <= 60 ){
            System.out.println("Keep trying");
        }

        System.out.println("========================================");

        if(score >= 60){
            System.out.println("Congrats");
        }else{
            System.out.println("Keep trying");
        }


    }

}
