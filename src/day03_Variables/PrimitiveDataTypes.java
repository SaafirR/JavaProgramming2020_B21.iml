package day03_Variables;

public class PrimitiveDataTypes {

    public static void main(String[] args) {
         //score is 85
        int score = 85;

        System.out.println(score);
        System.out.println("score");

        // long number0 = 9999999999;
        //  int is preferred, compiler takes it as int,and the number 9999999999 is out of range

        long number1 = 9999999999L;
        double decimal = 0.5;
        // float float1 = 0.5; double is perferred for decimals, complier takes it as, double' range is greater

        float float2 = 0.5f;
        System.out.println(float2); // 0.5

        /*
        in future:
                for integers: int
                for decimal: double

         */


    }

}
